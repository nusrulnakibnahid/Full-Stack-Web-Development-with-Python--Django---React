#Built-in Math Functions
a = min(5, 10, 15, 20, 25, 30, 35, 40, 45, 50)
print("Min Number: ", a)

b = max(5, 10, 15, 20, 25, 30, 35, 40, 45, 50)
print("Max Number: ",b)

# abs() function returns the positive value of a number
c = abs(-5)
print("Absolute value of -5 is: ", c)

# pow() function returns the value of x to the power of y
d = pow(2, 3)
print("2 to the power of 3 is: ", d)


# round() function rounds a number to the nearest integer
e = round(3.7)
print("Round of 3.7 is: ", e)


# import math module to use math functions
import math

x = math.sqrt(16)
y = math.ceil(3.7)

print("Square root of 16 is: ", x)
print("Ceiling of 3.7 is: ", y)
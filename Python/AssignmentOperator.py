# Basic Assignment
x = 10  # Assign 10 to x
print(x)  # Output: 10

# Common Assignment Operators

# 1. Simple Assignment (=)
name = "John"
age = 25

# 2. Add and Assign (+=)
x = 5
x += 3  # Same as: x = x + 3
print(x)  # Output: 8

# 3. Subtract and Assign (-=)
y = 10
y -= 2  # Same as: y = y - 2
print(y)  # Output: 8

# 4. Multiply and Assign (*=)
z = 4
z *= 2  # Same as: z = z * 2
print(z)  # Output: 8

# 5. Divide and Assign (/=)
a = 16
a /= 2  # Same as: a = a / 2
print(a)  # Output: 8.0

# 6. Modulus and Assign (%=)
b = 10
b %= 3  # Same as: b = b % 3
print(b)  # Output: 1

# 7. Power and Assign (**=)
c = 2
c **= 3  # Same as: c = c ** 3
print(c)  # Output: 8
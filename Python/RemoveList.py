#Remove Specified Item
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
thislist.remove("banana")
print(thislist)

#pop Specified Index
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
thislist.pop(2)
print(thislist)


#del Specified Index
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
del thislist[0]
print(thislist)


#clear All Items
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
thislist.clear()
print(thislist)